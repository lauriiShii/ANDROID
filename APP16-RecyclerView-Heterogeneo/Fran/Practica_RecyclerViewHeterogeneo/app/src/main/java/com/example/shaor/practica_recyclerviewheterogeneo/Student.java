package com.example.shaor.practica_recyclerviewheterogeneo;

public class Student implements ListElement {

    private String name;
    private int age;

    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public int getType() {
        return R.layout.activity_main_studentitem;
    }
}
